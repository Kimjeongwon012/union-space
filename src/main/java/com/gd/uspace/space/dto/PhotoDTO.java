package com.gd.uspace.space.dto;

public class PhotoDTO {
	
	private int space_no;
	private String space_file_name;
	private String space_update_name;
	private int space_isMain;
	
	public int getSpace_no() {
		return space_no;
	}
	public void setSpace_no(int space_no) {
		this.space_no = space_no;
	}
	public String getSpace_file_name() {
		return space_file_name;
	}
	public void setSpace_file_name(String space_file_name) {
		this.space_file_name = space_file_name;
	}
	public String getSpace_update_name() {
		return space_update_name;
	}
	public void setSpace_update_name(String space_update_name) {
		this.space_update_name = space_update_name;
	}
	public int getSpace_isMain() {
		return space_isMain;
	}
	public void setSpace_isMain(int space_isMain) {
		this.space_isMain = space_isMain;
	}
	
		

}
